import gsap from 'gsap'

const heroText = ref(null)

onMounted(async () => {
  const { TextPlugin } = await import('gsap/TextPlugin')
  gsap.registerPlugin(TextPlugin)

  const firstText = 'Web Dev'
  const secondText = 'Digital Designs'

  gsap.timeline({ repeat: -1 }) 
    .to(heroText.value, {
      duration: 0.5,
      opacity: 1,
      ease: 'power2.out',
    })
    .to(heroText.value, {
      duration: firstText.length * 0.1,
      text: {
        value: firstText,
        delimiter: '',
      },
      ease: 'none',
    })
    .to(heroText.value, {
      delay: 3, 
      duration: firstText.length * 0.1,
      text: {
        value: '',
        delimiter: '',
      },
      ease: 'none',
    })
    .to(heroText.value, {
      duration: secondText.length * 0.1,
      text: {
        value: secondText,
        delimiter: '',
      },
      ease: 'none',
    })
    .to(heroText.value, {
      delay: 3, 
      duration: secondText.length * 0.1,
      text: {
        value: '',
        delimiter: '',
      },
      ease: 'none',
    })
})
